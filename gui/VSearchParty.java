    private void buscarFestas()
    {
        TagsListModel me;
        me = (TagsListModel) listaFestas.getModel();
        
        TagsListModel mu;
        mu = (TagsListModel) listaUbicacions.getModel();
        
        java.util.List<ContainerTag> etiquetas = new java.util.ArrayList<>();
        
        java.util.List<Party> festas = new java.util.ArrayList<>();
        
        try
        {
            
            Date data = null;
            
            if(!txtData.getText().isEmpty())
            {
                data = new SimpleDateFormat("dd/MM/yyyy").parse(txtData.getText());
            }  

            if(checkHabilitarUbicacion.isSelected())
            {
                if(mu.getSize() > 0)
                {
                    if(listaUbicacions.getSelectedIndex() != -1)
                    {
                        Container<Location> contenedor = mu.getElementAt(listaUbicacions.getSelectedIndex()).getContenedor();
                        Location ubicacion = contenedor.getContido();
                        //festas = fa.buscarFestasUbicacion(txtNomeFesta.getText(), txtOrganizador.getText(), txtTematica.getText().isEmpty() ? null : txtTematica.getText().toLowerCase(),
                                //(String)comboPrivacidade.getSelectedItem(), data, ubicacion, (float)slideRadio.getValue());
                    }
                }
            }
            else
            {
                festas = this.fa.buscarFestas(this.fa.getUsuarioActual().getId(),txtNomeFesta.getText(), txtOrganizador.getText(), txtTematica.getText().isEmpty() ? null : txtTematica.getText().toLowerCase(), (String)comboPrivacidade.getSelectedItem(), (java.sql.Date) data);
            }

            for(Party festa : festas)
            {
                Container<Party> contenedor = new Container<>(festa);
                etiquetas.add(new ContainerTag(contenedor));
            }
            me.setElementos(etiquetas);
        }
        catch(ParseException pe)
        {
            fa.muestraExcepcion("Introduce unha fecha no formato: dd/MM/yyyy");
        }
    }
    
