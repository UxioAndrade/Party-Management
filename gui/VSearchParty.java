    public void abrirVentanaFesta()
    {
        TagsListModel me;
        Boolean organizador=false;
        me = (TagsListModel) listaFestas.getModel();
        
        if(me.getSize() > 0)
        {
            if(listaFestas.getSelectedIndex() != -1)
            {
                Container<Party> contenedor = (me.getElementAt(listaFestas.getSelectedIndex())).getContenedor();
                Party festa = contenedor.getContido();
                if(this.fa.consultarOrganizador(festa.getId()).equals(this.fa.getUsuarioActual().getId())){
                    organizador=true;
                }
                VParty vf=new VParty(this.fa.getFachadaGui().getVPrincipal(), true,this.fa, festa.getId(),this.fa.consultarPosts(festa.getId()),organizador, (String)this.comboPrivacidade.getSelectedItem());
                vf.setVisible(true);
            }
        }
    }
    
   
                
